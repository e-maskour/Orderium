export function translateUomCode(code: string | undefined | null, language: string): string {
  if (!code) return language === 'ar' ? UOM_AR['UNIT'] : UOM_FR['UNIT'];
  const map = language === 'ar' ? UOM_AR : UOM_FR;
  return map[code.toUpperCase()] ?? code;
}
